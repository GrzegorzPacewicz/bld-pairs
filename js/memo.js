export function getMemoWord(pair) {
  if (!pair || pair.length === 0) return null;
  if (pair.length === 1) return pair[0];
  const key = pair[0] + pair[1];
  const keyReverse = pair[1] + pair[0];
  return MEMO_WORDS[key] || MEMO_WORDS[keyReverse] || null;
}
